package narrowing;

public class NarrowTypeCast {
	 public static void main(String[] args) {

		//----------double to integer-----------------
		 double d = 10.5;
	     System.out.println(d);
	    
	    
	    int i = (int)d;
	    System.out.println(i);
	    
	    //-----------------int to short------------------
	    
	    int a = 7;
	    System.out.println(a);
	    
	    short s = (short)a;
	    System.out.println(a);
	    
	    //----------char to int----------------
	    
	    char c = 'm';
	    System.out.println(c);
	    
	    int e = (int)c;
	    System.out.println(e);
	    
	    //double to float----------------------
	    float g =(float)d;
	    System.out.println(g);
	    
	    
	    float f = (float)c;
	    
	    
	    
	}
}
